package metaenlace.citas_medicas.entitiesDTO;

public class UsuarioDTO {
	private Integer usuarioUserID;
	private String usuarioUsuario;
	private String usuarioNombre;
	private String usuarioApellidos;
	private String usuarioClave;
	/**
	 * @return the usuarioUserID
	 */
	public Integer getUsuarioUserID() {
		return usuarioUserID;
	}
	/**
	 * @param usuarioUserID the usuarioUserID to set
	 */
	public void setUsuarioUserID(Integer usuarioUserID) {
		this.usuarioUserID = usuarioUserID;
	}
	/**
	 * @return the usuarioUsuario
	 */
	public String getUsuarioUsuario() {
		return usuarioUsuario;
	}
	/**
	 * @param usuarioUsuario the usuarioUsuario to set
	 */
	public void setUsuarioUsuario(String usuarioUsuario) {
		this.usuarioUsuario = usuarioUsuario;
	}
	/**
	 * @return the usuarioNombre
	 */
	public String getUsuarioNombre() {
		return usuarioNombre;
	}
	/**
	 * @param usuarioNombre the usuarioNombre to set
	 */
	public void setUsuarioNombre(String usuarioNombre) {
		this.usuarioNombre = usuarioNombre;
	}
	/**
	 * @return the usuarioApellidos
	 */
	public String getUsuarioApellidos() {
		return usuarioApellidos;
	}
	/**
	 * @param usuarioApellidos the usuarioApellidos to set
	 */
	public void setUsuarioApellidos(String usuarioApellidos) {
		this.usuarioApellidos = usuarioApellidos;
	}
	/**
	 * @return the usuarioClave
	 */
	public String getUsuarioClave() {
		return usuarioClave;
	}
	/**
	 * @param usuarioClave the usuarioClave to set
	 */
	public void setUsuarioClave(String usuarioClave) {
		this.usuarioClave = usuarioClave;
	}
	
	
}
